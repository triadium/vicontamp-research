public class UYReference
{
    private static readonly IReadOnlyDictionary<int, string> classByIdMap;
    
    static UYReference() {
        classByIdMap = Enum.GetValues(typeof(UYClass)).Cast<UYClass>().ToDictionary(k => (int)k, v => Enum.GetName(typeof(UYClass), v));
    }
    
    public static string GetClassById(int Id)
    {
        return classByIdMap[Id] ?? "Unknown";
    }
}
